/**
  * @brief This function handles SPI2 global interrupt.
  */
void SPI2_IRQHandler(void)
{
  /* USER CODE BEGIN SPI2_IRQn 0 */
	Reset_BB_OK; flagBB_OK = 0; 	
	if(LL_SPI_IsActiveFlag_RXNE(SPI2))
  {    
		SPI2_RX_Callback();
  }
  else if(LL_SPI_IsActiveFlag_TXE(SPI2))
  {
    SPI2_TX_Callback();
  }
  else if(LL_SPI_IsActiveFlag_OVR(SPI2))
  {
    TX_SPI2[0] = 0xFF;
		TX_SPI2[1] = 0xFF;
		TX_SPI2[2] = 0xFF;
		TX_SPI2[3] = 0xFF;
		numtx=0; numrx=0;
		LL_SPI_TransmitData16(SPI2, TX_SPI2[0]);
    LL_SPI_EnableIT_RXNE(SPI2);
    LL_SPI_EnableIT_TXE(SPI2);
		Set_BB_OK; flagBB_OK = 1; 											// выставляем флаг готовности	
  }
  /* USER CODE END SPI2_IRQn 0 */
  /* USER CODE BEGIN SPI2_IRQn 1 */
//	if(T3 < 3){T3 = 0; 
//	flagSpi2 = 1;} 
//	else T3 = 0;

//	HAL_SPI_Receive_IT(&hspi2,(uint8_t*)RX_SPI2,2);
  /* USER CODE END SPI2_IRQn 1 */
}

